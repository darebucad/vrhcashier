if (!empty($_POST['btn_login'])) {

    $username = trim($_POST['username']);
    $password = trim($_POST['password']);

    if (empty($username) || empty($password)) {
      $login_error_message = 'Username and password must be filled out';
    }
    else{
        $user_id = $lib->Login($username, $password); // check user login

        if($user_id > 0){
            $_SESSION['user_id'] = $user_id; // Set Session
            header("location: /src/home.php"); // Redirect user to the profile.php
        }
        else{
            $login_error_message = 'Incorrect username or password';
        }
    }
  }

